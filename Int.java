package com.javaex.helloworld;

public class Int {
	public static void main(String[] args) {
		int myAge;
		myAge = 25;
		System.out.println(myAge);
		myAge = 26;
		System.out.println(myAge);
		
		int a=10;  
		int b=10;  
		System.out.println(a++ + ++a);//10+12=22  
		System.out.println(b++ + b++);//10+11=21  
	}

}
